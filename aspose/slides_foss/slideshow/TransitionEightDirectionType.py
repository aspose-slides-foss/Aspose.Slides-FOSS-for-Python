from __future__ import annotations
from enum import Enum
class TransitionEightDirectionType(Enum):
    """Represent eight direction transition types."""
    LEFT_DOWN = 'LeftDown'
    LEFT_UP = 'LeftUp'
    RIGHT_DOWN = 'RightDown'
    RIGHT_UP = 'RightUp'
    LEFT = 'Left'
    UP = 'Up'
    DOWN = 'Down'
    RIGHT = 'Right'
